import { readFileSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';

const input = join(process.cwd(), '../api/openapi/libif-api.json');
const output = join(process.cwd(), 'lib/generated/api-types.ts');
const document = JSON.parse(readFileSync(input, 'utf8'));
const schemas = document.components?.schemas ?? {};

function quote(value) {
  return JSON.stringify(value);
}

function refName(ref) {
  return ref.split('/').at(-1);
}

function schemaType(schema) {
  if (!schema) return 'unknown';
  const nullable = schema.nullable ? ' | null' : '';
  if (schema.$ref) return `components['schemas'][${quote(refName(schema.$ref))}]${nullable}`;
  if (schema.oneOf || schema.anyOf) return [...(schema.oneOf ?? []), ...(schema.anyOf ?? [])].map(schemaType).join(' | ') || 'unknown';
  if (schema.allOf) return schema.allOf.map(schemaType).join(' & ') || 'unknown';
  if (schema.enum?.length) return `${schema.enum.map(quote).join(' | ')}${nullable}`;
  if (schema.type === 'array') {
    const itemType = schemaType(schema.items);
    return `${itemType.includes(' | ') ? `(${itemType})` : itemType}[]${nullable}`;
  }
  if (schema.type === 'integer' || schema.type === 'number') return `number${nullable}`;
  if (schema.type === 'boolean') return `boolean${nullable}`;
  if (schema.type === 'string') return `string${nullable}`;
  if (schema.type === 'object' || schema.properties) return `${objectType(schema)}${nullable}`;
  return `unknown${nullable}`;
}

function objectType(schema) {
  const props = schema.properties ?? {};
  const required = new Set(schema.required ?? []);
  const lines = Object.entries(props).map(([name, prop]) => `    ${quote(name)}${required.has(name) ? '' : '?'}: ${schemaType(prop)};`);
  return lines.length ? `{
${lines.join('\n')}
  }` : 'Record<string, unknown>';
}

function responseContent(operation) {
  const responses = operation.responses ?? {};
  const lines = Object.entries(responses).map(([status, response]) => {
    const content = response.content ?? {};
    const mediaLines = Object.entries(content).map(([mediaType, media]) => `          ${quote(mediaType)}: ${schemaType(media.schema)};`);
    return `      ${quote(status)}: {
        content: {
${mediaLines.join('\n') || '          "application/json": unknown;'}
        };
      };`;
  });
  return lines.join('\n');
}

function requestBody(operation) {
  const content = operation.requestBody?.content;
  if (!content) return '';
  const mediaLines = Object.entries(content).map(([mediaType, media]) => `          ${quote(mediaType)}: ${schemaType(media.schema)};`);
  return `
      requestBody: {
        content: {
${mediaLines.join('\n')}
        };
      };`;
}

function parameters(operation) {
  const params = operation.parameters ?? [];
  if (!params.length) return '';
  const grouped = params.reduce((acc, param) => {
    acc[param.in] ??= [];
    acc[param.in].push(param);
    return acc;
  }, {});
  const lines = Object.entries(grouped).map(([location, locationParams]) => {
    const required = new Set(locationParams.filter((param) => param.required).map((param) => param.name));
    const props = locationParams.map((param) => `          ${quote(param.name)}${required.has(param.name) ? '' : '?'}: ${schemaType(param.schema)};`);
    return `        ${location}: {
${props.join('\n')}
        };`;
  });
  return `
      parameters: {
${lines.join('\n')}
      };`;
}

const schemaLines = Object.entries(schemas).map(([name, schema]) => `    ${quote(name)}: ${schemaType(schema)};`);
const pathLines = Object.entries(document.paths ?? {}).map(([path, pathItem]) => {
  const methodLines = Object.entries(pathItem)
    .filter(([method]) => ['get', 'post', 'put', 'patch', 'delete'].includes(method))
    .map(([method, operation]) => `    ${method}: {${parameters(operation)}${requestBody(operation)}
      responses: {
${responseContent(operation)}
      };
    };`);
  return `  ${quote(path)}: {
${methodLines.join('\n')}
  };`;
});

const source = `// Generated by apps/web/scripts/generate-api-types.mjs from apps/api/openapi/libif-api.json.
// Do not edit by hand.

export interface components {
  schemas: {
${schemaLines.join('\n')}
  };
}

export interface paths {
${pathLines.join('\n')}
}
`;

mkdirSync(dirname(output), { recursive: true });
writeFileSync(output, source);
console.log(`Generated ${output}`);
